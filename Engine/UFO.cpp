UFO::UFO(int start_x, int start_y, int start_speed)
{
	x = start_x;
	y = start_y;
	speed = start_speed;
}

void UFO::DrawUFO(Graphics& gfx)
{
		if (lightswitch > 240)
		{
			DrawUFO_0(x, y, gfx);
		}
		if (lightswitch <= 240 && lightswitch > 180)
		{
			DrawUFO_1(x, y, gfx);
		}
		if (lightswitch <= 180 && lightswitch > 120)
		{
			DrawUFO_2(x, y, gfx);
		}
		if (lightswitch <= 120 && lightswitch > 60)
		{
			DrawUFO_3(x, y, gfx);
		}
		if (lightswitch <= 60)
		{
			DrawUFO_4(x, y, gfx);
		}
		if (lightswitch <= 0)
		{
			lightswitch = 300;
		}
}

void UFO::movement(const Keyboard& kbd)
{
	//Controls
	if (kbd.KeyIsPressed(VK_UP))
		y -= speed;
	if (kbd.KeyIsPressed(VK_DOWN))
		y += speed;
	if (kbd.KeyIsPressed(VK_LEFT))
		x -= speed;
	if (kbd.KeyIsPressed(VK_RIGHT))
		x += speed;

	//Boundaries
	const int left = x - speed;
	const int right = x + speed + width;
	const int top = y - speed;
	const int bottom = y + speed + height;
	if (left < 0)
		x = 0;
	if (right > 799)
		x = 799 - width;
	if (top < 0)
		y = 0;
	if (bottom > 546 && !base_open)
	{
		y = 546 - height;
	}
	if (base_open)
	{
		if (bottom > 546 && (left < 367 || right > 425))
			y = 546 - height;
		if (left >= 367 && right <= 425)
		{
			if (bottom > 596)
				y = 596 - height;
			if (left < 377 && bottom > 552)
				x = 377;
			if (right > 414 && bottom > 552)
				x = 414 - width;
		}
	}
}

void UFO::DrawFire(Graphics& gfx)
{
	if((onfire <= 100 && onfire > 90) || (onfire <= 80 && onfire > 70) || (onfire <= 60 && onfire > 50)
		|| (onfire <= 40 && onfire > 30) || (onfire <= 20 && onfire > 10))
	{
		DrawFire_0(x, y, gfx);
	}
	if ((onfire <= 90 && onfire > 80) || (onfire <= 70 && onfire > 60) || (onfire <= 50 && onfire > 40)
		|| (onfire <= 30 && onfire > 20) || (onfire <= 10 && onfire > 0))
	{
		DrawFire_1(x, y, gfx);
	}
}

